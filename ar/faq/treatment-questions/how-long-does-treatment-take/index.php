          <h3>تقدمي وفقاً لقدرتك</h3>
          <p>تتطلب بعض النساء مع تنافس متطلبات الحياة نهج أبطأ. بغض النظر عن وتيرة، والحل الناجح هو نتيجة طبيعية مع اختراق خالية من الألم والجماع استعادة كامل في نهاية <a href="../index.html#10-steps">المهنيين العلاج</a>.</p>
          <p>لا ترتبط شدة التشنج المهبلي الظاهرة بالضرورة مع طول فترة العلاج. فسهولة الشفاء تعتمد إلى حد كبير على الفرد وظروف الفريدة. أوردت بعض النساء أنهن استطعن التخلص من التشنج المهبلي الأولي والحصول على جماع ناجحفي أقل من أسبوع بعد مرور سنين بدا الجماع فيها مستحيلا.</p>

          <h3>الانتهاء من البرنامج</h3>
          <p>أما البعض الآخر فأوردن أن مدة العلاج دامت لعدة شهور، على الرغم من أن التشنج المهبلي لديهن كان يبدو أقل حدة عموما، إذا دامت العملية أكثر من بضعة أشهر فغالبا ما توجد قضايا عاطفية أو أخرى متعلقة بالدوافع أو <a href="../how-can-i-stay-motivated/index.html">التهرب</a> أو متعلقة بحل مشاكل معقدة قد يكون لها علاقة بسيطة مباشرة بخطوات العلاج. مصحوبا بإيلاج خال من الألم وجماع سليم سير العلاج تحتاج بعض النساء المنشعلات بمطالب الحياة المتزاحمة إلى نهج أبطأ، لكن بغض النظر عن الوتيرة يبقى الحل الناجح هو النتيجة الطبيعية في نهاية.</p>
