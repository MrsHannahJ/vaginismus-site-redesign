          <h1>ما هو عسر الجماع أو ألم الجماع؟ ما هي العلاقة بين ألم الجماع والتشنج المهبلي؟</h1>
          <h3>يمكن لأي نوع من أنواع ألم الجماع بأن يحرض على التشنج المهبلي (ألم مستمر خلال ممارسة الجنس أو لدى الإدخال بسبب تضييق مهبلي غير طوعي).</h3>

          <h3>ألم الجماع</h3>
          <p>ألم الجماع هو مصطلح طبي يعني ببساطة أن “الجماع مؤلم”. وهو مصطلح عام يستخدم لوصف كل أنواع الألم الجنسي. وقد يحدث الألم الجنسي عند الإيلاج، وخلال الجماع، و/أو بعد الجماع. ويمكن أن يوجد في أي مكان في المنطقة التناسلية – البظر، أو الشفيرين الصغيرين، أو في الفرج ، إلخ. ويمكن وصف الألم بأنه حاد لاذع وحارق، مع اصطدام، أو تشنج أو يمكن وصفه بطرق أخرى.</p>

          <h3>العلاقة بين ألم الجماع والتشنج المهبلي</h3>
          <p>هناك العديد من الأسباب لألم الجماع، <a href="/ar/faq/general-questions/what-is-vaginismus/index.php">والتشنج المهبلي</a> هو أحدها. وما يميز التشنج المهبلي بشكل فريد هو تضييق غير طوعي في المهبل بسبب  <a href="/ar/vaginismus-causes/index.php#pc-muscles">العضلات العانية العصعصية (ع ع)</a>. وقد يؤدي هذا التضييق إلى انزعاج جنسي عند محاولات الجماع ، والإحساس بحرقة أو تضييق أو ألم أو عدم القدرة على الإيلاج.</p>
          <p>ويتشابه التشنج المهبلي أيضا مع جميع الأشكال الأخرى من ألم الجماع حيث أن أي نوع من الألم الجنسي الاعتيادي قد يؤدي إلى التشنج المهبلي. وفي مثل هذه الحالات، يصبح التشنج المهبلي حالة معقدة من الانزعاج أو الألم بالإضافة إلى مشكلة الألم الأصلية، وعادة ما تستمر حتى بعد زوال المشكلة الأصلية أو السيطرة عليها. وفي الواقع، قد تخلط المرأة بين مشاكل التشنج المهبلي المستمرة ومشكلة الألم الأصلية، معتقدة أن المشكلة الأصلية لم تحل بعد.</p>
          <p>“ينبغي اعتبار التشنج المهبلي كمساهم محتمل أو ربما السبب الرئيسي في حالة وجود أي نوع من الألم الجنسي المستمر والذي لا يبدو أن له أي سبب عضوي”.</p>
          <h3>إيجاد الأدلة على وجود التشخيص</h3>
          <p>وقد يصعب <a href="/ar/vaginismus-diagnosis/index.php">تشخيص</a> الألم الجنسي وعلاجه عندما يكون هناك تداخل من مضاعفات التشنج المهبلي. ويمكن أن يكون الأمر محبطا لكل من الأطباء والمرضى. فعلى سبيل المثال امرأة تعرضت لالتهاب مؤقت (أحد أشكال ألم الجماع) فتسبب بحدوث تشنج مهبلي؛ فربما بحلول الوقت لزيارة الطبيب يكون الالتهاب قد زال من تلقاء ذاته، ولكن التشنج المهبلي باق. وعندما يحاول الطبيب اكتشاف مصدر الألم، قد يبدو له أنه ليس هناك أي سبب عضوي واضح لأن المريضة لا تزال تعاني من ألم التشنج المهبلي وليس من الالتهاب الذي حدث أولا. وعلاوة على ذلك، قد يبدو وصف المرأة للألم مشوشا لأنه كان يوجد بالفعل مشكلتان تسببان لها الألم في أوقات متفاوتة.</p>
          <p>عندما يوجد نوعان من الألم الجنسي في الوقت نفسه، وثانيهما هو التشنج المهبلي، فإن معظم المتخصصين ينصحون بمعالجة الحالة الطبية الأصلية أولا، وبعد ذلك اتخاذ الخطوات اللازمة <a href="/ar/vaginismus-treatment/index.php"> لمعالجة</a> التشنج المهبلي.</p>
          <h3>أساطير حول المهبل العلاج</h3>
          <p>تحذير: ظن كثير من الأطباء ومرضاهم أن الجراحات والعمليات الطبية ضرورية في حين أن الحالة الفعلية هي ببساطة تشنج مهبلي الذي كان من الممكن معالجتها بدون أية عمليات جراحية. ويجب على المريضات توخي الحذر والتماس رأيا ثان لأطباء ذوي خبرة في علاج النساء اللواتي يعانين من الألم الجنسي ولا سيما إذا كانت المريضة تفكر في خيارات العلاج الجراحي الموسع، حيث أن <a href="/ar/vaginismus-treatment/index.php">علاج التشنج المهبلي</a> عادة لا يشتمل على أي جراحات.</p>
          <h3>أسباب محتملة للألم خلال ممارسة الجنس (ألم الجماع)</h3>
          <ul>
            <li>ألم الفرج/ألم دهليز الفرج</li>
            <li>أورام الجهاز التناسلي أو الحوض</li>
            <li>أكياس المبيض</li>
            <li>التهاب الإحليل</li>
            <li>التهاب المسالك البولية</li>
            <li>التهاب المثانة الخلالي</li>
            <li>ضمور المهبل (التهاب المهبل الضموري)</li>
            <li>جفاف المهبل</li>
            <li>عدم كفاية التزليق المهبلي</li>
            <li>صدمة الولادة ( ما بعد الولادة)</li>
            <li>الفرج سرطان</li>
            <li>العلاج الإشعاعي</li>
            <li>الأخماج المهبلية/مهيجات – فطرية و جرثومية، وبعض الأمراض المنقولة جنسيا، الخ.</li>
            <li>حالات البشرة – التصلب الحزازي، الحزاز المسطح، الأكزيما، الصداف</li>
            <li>الآثار الجانبية لبعض الأدوية</li>
            <li>الإصابات في المنطقة الحوضية/التناسلية</li>
            <li>الأعراض الأعراض والمتعلقة بانقطاع الطمث و/أو الشيخوخة</li>
            <li>الحساسية تجاه بعض الملابس، والواقي الذكري، والحبوب الفوارة المانعة للحمل، و/أو مبيدات الحيوانات المنوية</li>
            <li>ألم فحص الحوض</li>
            <li>الصدمات النفسية بسبب الاعتداء الجنسي</li>
            <li>تشويه الأعضاء التناسلية للإناث (FMG)</li>
            <li>داء البطانة الرحمية الهاجرة</li>
            <li>الداء الحوضي الالتهابي</li>
          </ul>
          <p>يجب الانتباه إلى أن التشنج المهبلي يمكن أن يوجد أيضا بالإضافة إلى الحالات السابقة أو يمكن أن يستمر في الوجود حتى بعد معالجتها أو السيطرة عليها.</p>
