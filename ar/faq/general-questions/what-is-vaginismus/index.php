          <h3>التشنج المهبلي</h3>
          <p>التشنج المهبلي هو حالة يحدث فيها تضييق غير طوعي للمهبل خلال محاولة الجماع. وما يسبب التضييق في الحقيقة هو انقباضات غير طوعية ل <a href="/ar/vaginismus-causes/index.php#pc-muscles">عضلات قاع الحوض</a> المحيطة بالمهبل، فلا تسيطر المرأة بشكل مباشر على التضييق ولا ‘ترغب’ في حدوثه؛ إنما هو ردة فعل لاطوعية للحوض. لا تسيطر المرأة مباشرة أو تتحكم في الانقباض ليحدث، لكنه رد فعل للحوض يحدث تلقائياً. وربما لا تدرك المرأة حتى أن ردة فعل العضلات هي التي تسبب التضييق أو مشكلة الإيلاج.</p>
