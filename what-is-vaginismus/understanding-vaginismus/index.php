          <p>Vaginismus is a condition where there is involuntary tightness of the vagina during attempted intercourse. The tightness is actually caused by involuntary contractions of the <a href="../../../vaginismus-causes/index.html#pc-muscles">pelvic floor muscles</a> surrounding the vagina. The woman does not directly control or <em>will</em> the tightness to occur; it is an involuntary pelvic response. She may not even have any awareness that the muscle response is causing the tightness or penetration problem.</p>
          <p>In some cases, vaginismus tightness may begin to cause burning, pain, or stinging during intercourse. In other cases, penetration may be difficult or completely impossible. Vaginismus is the main cause of <a href="../../../vaginismus-symptoms/index.html#unconsummated-marriages">unconsummated relationships</a>. The tightness can be so restrictive that the opening to the vagina is &#8220;closed off&#8221; altogether and the man is unable to insert his penis. The pain of vaginismus ends when the sexual attempt stops, and, usually, intercourse must be halted due to pain or discomfort.</p>
