          <h3>Uygun Bir Boyut Aralığı</h3>
          <p>Fazlasıyla büyük, fazlasıyla küçük, boyutlar arasında büyük farklılıklar olan dilatörler geçişleri zorlaştırır. Ya da yeterli boyut çeşitliliği olmadığında vajinismus tedavisi gereksiz yere zorlaşır. İdeal olarak, dilatör setlerindeki dilatörlerin en küçüğünün çapı 19mm.’den daha büyük değildir ve çap yavaş yavaş ilerleyerek bir erkeğin tamamen dik penisine [yaklaşık 35 mm.’ye] erişir. Uzunluk, vajinayı çevreleyen <a href="../../../vaginismus-causes/index.html#pc-muscles">pubococcygeus (PC) kas bağları</a> nı geçebilmek için en az 76mm. olmalıdır. Bir sette ideal olarak uzunluk bakımından en az dik bir penisin uzunluğunda olan ve başka sorunlar ele alındığı daha derin vajinal duvar dokularına tedavi sağlamak için yaklaşık olarak 152 mm. uzunluğunda bir dilatör olmalıdır. Özellikle de pelvik kas kontrolü öğreten dinamik bir program izlenmeden direk olarak tam boy dilatörlere (ya da bazen vibratör ya da dildolara) geçmeye çalışan kadınlar genelde büyük bir hüsran yaşarlar.</p>

