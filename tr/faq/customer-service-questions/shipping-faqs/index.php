          <h4>Hangi gönderi yöntemleri kullaniliyor?</h4>
          <p>Siparisinizi verirken, online olarak nakliyatin teslim edilecegi yeri seçtiginizde siparis seçenekleri orada belirtilen ülkeler için normalde açiktir. Tipik seçenekler arasında ABD Posta Servisi (USPS) ve DHL yer almaktadır. Bu süreç içinde sunulanlardan baska seçenekler istiyorsaniz, lütfen islemi tamamlamadan önce bizimle dogrudan <a href="/tr/contact-us/index.php">iletisime geçin.</a></p>

          <h4>Ürünleriniz nereden gönderiliyor?</h4>
          <p>Bütün ürünler Kaliforniya, ABD’den gönderilmektedir.</p>

          <h4>Paketin üzerinde ne görünüyor? Gönderi listesinde ve/veya gümrük bildirim formunda paketin içinde ne oldugu yazili?</h4>
          <p>Görevimiz, cinsel ağrı sorunları yaşayan çiftlere yardım sunmaktır. Bu yüzden, müşterilerimizin gizliliğini ve mahremiyetini korumak amacıyla ayrıntılara dikkat ediyoruz. Siparişiniz sade bir ambalaj içerisinde gelecektir. Vajinismus, vaginismus.com kelimeleri veya ilgili başka bir terim kutunun veya irsaliyenin üzerinde yer almaz. Gönderici olarak genel bir gönderim şirketinin adı kullanılır. Gümrük bildirim formlarında ürün adları gizliliği korumak adına eğitim seti, plastik parçalar veya benzer ifadeler olarak yer alacaktır.</p>

          <h4>Uluslararasi paket gönderiyor musunuz?</h4>
          <p>Evet, tüm dünyaya gönderim yapıyoruz. Arzumuz mümkün oldugu kadar çok sayida vajinismuslu kadina yardim etmek oldugundan ürünleri, ne kadar kapali olursa olsun, her ülkeye göndermeye çalismayi istiyoruz. Bazı ülkeler için bir doktordan alınacak “gereklilik belgesi” faydalı olabilir.</p>

          <h4>Paketler için gümrük verilecek mi?</h4>
          <p>Her ülkenin uygun gümrük ücretlerini ya da vergilerini belirlemekte kullandigi kendi kurallari vardir. Bu ücretlerin hepsinden ve tasiyici firmanin gümrük islem ücretlerinden müsteri sorumludur. Tüm ülkeler kendi gümrük harçlarını almaktadır.</p>

