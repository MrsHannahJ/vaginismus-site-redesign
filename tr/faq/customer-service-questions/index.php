          <h3>Siparis SSS</h3>
          <h4>Ben sipariş ne para fatura için kullanılır? Nasıl dönüşüm hesaplayabilirsiniz?</h4>
          <p>Tüm fiyatlara ABD doları olarak yapılır. sipariş sırasında, kredi kartı şirketi sizin için otomatik olarak dönüştürme gerçekleştirmek olacaktır. kabaca önceden para birimi dönüştürme hesaplamak istiyorsanız, bu <a href="http://www.xe.com/" target="_blank">ücretsiz</a> hesap makinesini kullanabilirsiniz.</p>

          <h4>Hangi ödeme yöntemlerini kabul ediyorsunuz?</h4>
          <p>Kredi kartiyla ya da bizi dogrudan arayarak bir siparis vermek, siparis vermenin en hizli ve en etkin iki biçimidir. Visa, MasterCard, American Express, Discover ve Paypal ile yapılan ödemeleri kabul ediyoruz ve ayrıca Western Union para havalesi ve Amerikan Doları posta havalelerini de (garantili çekler ya da başka para araçlarını) kabul edebiliyoruz. Bu tür ödemeler konusunda daha çok bilgi için bizimle iletisime geçin.</p>

          <h4>PayPal nedir? Nasıl Işler?</h4>
          <p>Ana kredi kartlarindan biriyle dogrudan ödeme yapamayan ya da yapmak istemeyenler için PayPal popüler bir alternatif güvenli online ödeme islem yöntemidir (bkz. <a href="http://paypal.com/" target="_blank">www.paypal.com</a>). PayPal, kullanıcıların banka hesapları, çeşitli kredi kartları ve diğer seçenekler gibi birçok değişik yöntem kullanarak ödeme yapmalarını sağlar. PayPal hesabiniz oldugunda, ödeme seçeneginiz olarak sadece PayPal’i seçerek online ürün siparisi verebilirsiniz.</p>

          <h4>Eger kredi kartim yoksa nasil siparis verebilirim?</h4>
          <p>Kredi kartlarının yanı sıra başka ödeme yöntemlerini de kabul ediyoruz: PayPal (çevrimiçi hesap ya da e-çekler), Amerikan Doları para havaleleri (garantili çekler ya da başka para araçları) ya da Western Union para havaleleri. Bu son iki hizmetten birini istemek için lütfen bizimle dogrudan <a href="/tr/contact-us/index.php">iletisime geçin</a>. Ihtiyaci olan bütün kadinlar için bir çözüm bulmak için elimizden geleni yapariz.</p>

          <h4>Internet üzerinden siparis vermekten çekiniyorum. Siteniz güvenli mi?</h4>
          <p>Evet, sizin gönül rahatligi içinde olmaniz için var olan en yüksek güvenlik yöntemlerini kullaniyoruz. Norton Symantec İnternet üzerinden ticaret güvenliğinde lider bir kuruluştur ve çevrimiçi mağazamız için tam şifreleme ve güvenlik sağlar.</p>

          <h3>İade SSS</h3>
          <h4>Dilatörleri kullanmazsam geri verip parami geri alabilir miyim?</h4>
          <p>Hayir, vajinal dilatör ürününde iade kabul edemiyoruz. Saglik ve güvenlik nedenlerinden ötürü ve FDA’nin vajinaya sokulan gereçler konusundaki kurallarina uymak için, bir müsteriye satilan vajinal dilatörler yeniden stoklanamaz ve yeniden satilmalari yasaktir.</p>

          <h4>Açilmamis kitap ya da videolar konusundaki iade politikaniz nedir?</h4>
          <p>Paranizin tamamini geri almak için açilmamis kitap ya da videolari satin aldiktan sonraki 30 gün içinde, orijinal paketlerinde iade edebilirsiniz. Ancak bunlara yükleme ve nakliye ücretleri ve $10 yeniden stoklama ücreti eklenecektir. Bu hizmetten yararlanmak için lütfen bizimle dogrudan iletisime geçin.</p>

