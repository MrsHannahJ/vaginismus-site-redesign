          <h1>Disparöni nedir? Disparöni ve vajinismus arasındaki bağlantı nedir?</h1>
          <h3>Genel seks acısı olan disparöni, vajinanın istemsiz sıkılığından kaynaklanan seks acısı ve giriş sorunu olan vajinismusu tetikleyebilir.</h3>

          <h3>Disparöni</h3>
          <p>Disparöni basitçe “acılı cinsel birleşme” anlamına gelen tıbbi bir terimdir. Her türlü cinsel acıyı tanımlamak için kullanılan genel bir terimdir. Giriş sırasında, cinsel birleşme sırasında ve/veya cinsel birleşme sonrasında cinsel acı hissedilebilir. Klitoris, labia ya da vajina vb. gibi kadının cinsel organının herhangi bir yerinde acı hissedilebilir. Hissedilen acı, keskin, batan, yanan, vuran, kramplar şeklinde ya da başka şekillerde tanımlanabilir.</p>

          <h3>Disparöni Ve Vajinismus Arasındaki Bağlantı</h3>
          <p>Cinsel acının (disparöni) birçok nedeni vardır ve <a href="/tr/faq/general-questions/what-is-vaginismus/index.php">vaginismus</a> da bunlardan biridir. Vajinismus özel olarak, vajinanın <a href="/tr/vaginismus-causes/index.php#pc-muscles">pubococcygeus (PC) kasları</a> ndan ötürü istemsiz sıkılığı olarak nitelendirilir. Cinsel birleşme çabalarında, vajinal sıkılık cinsel rahatsızlığa, yanma, sıkılık, acı ya da giriş yapamamak gibi rahatsızlıklara neden olabilir.</p>
          <p>Vajinismus disparöninin bütün diğer formlarıyla da yakından bağlantılıdır çünkü herhangi bir genel seks acısı vajinismusu tetikleyebilir. Bu durumlarda vajinismus özgün acı sorununa ek olarak komplikasyon yaratan bir rahatsızlık ve acı durumu haline gelir ve özgün sorun çözümlense ya da kontrol altına alınsa da tipik olarak devam eder. Hatta bir kadın, özgün sorunun hâlâ çözülmediğine inanarak vajinismusun devam eden sorununu özgün acı sorunuyla karıştırabilir.</p>
          <p><em>Vajinismus devam eden ve hiçbir fiziksel nedeni yok gibi gözüken her türlü cinsel acının var olduğu durumda buna katkıda bulunması mümkün olan sorun ya da belki de bunun ana nedeni olarak düşünülmelidir.</em></p>
          <h3>Bir Tanı için kanıtlar bulma</h3>
          <p>Vajinismustan ötürü örtüşen komplikasyonlar olduğunda cinsel acıyı <a href="/tr/vaginismus-diagnosis/index.php">teşhis</a> ve tedavi etmek epey zor olabilir. Bu durum hem doktorları, hem de hastaları sıkıntıya düşürebilir. Örneğin, geçici bir enfeksiyonun (bir disparöni biçimi) vajinismusu tetiklediği bir kadını düşünelim. Belki de kadın doktora gittiğinde enfeksiyon yok olmuştu ama vajinismus devam ediyordu.</p>
          <p>Hasta artık özgün enfeksiyondan değil, vajinismustan ötürü acı duymaya devam ettiğinden, doktor acının nedenini keşfetmeye çalıştığında, görünürde hiçbir fiziksel neden olmayabilir. Ayrıca aslında farklı zamanlarda iki tane acıya neden olan sorun olduğundan kadının acıyı tanımlaması kafa karıştırıcı gözükebilir.</p>
          <p>Aynı zamanda iki değişik cinsel acı var olduğunda ve ikincisi de vajinismus olduğunda, uzmanların çoğu özgün tıbbi sorunun çözülmesini ve ondan sonra da vajinismusu <a href="/tr/vaginismus-treatment/index.php">tedavi etmek</a> için gerekli adımların atılmasını önerir.</p>
          <h3>Vajinismus Tedavisi Hakkında Mitler</h3>
          <p>Dikkat: Aslında durum hiçbir invasif prosedür olmadan çözümlenecek basit bir vajinismus durumu olduğu halde, doktorlar ve hastaları ameliyatlar ve tıbbi prosedürlerin gerekli olduğunu düşünmüşlerdir.</p>
          <p>Özellikle de, invasif ameliyat bir tedavi seçeneği olarak sunulduğunda hastaların kadınların cinsel acı rahatsızlıklarını tedavi etmekte deneyimli doktorlardan ikinci bir görüş almaları gerekir. <a href="/tr/vaginismus-treatment/index.php">Vajinismus tedavisi</a> normalde ameliyat içermez.</p>
