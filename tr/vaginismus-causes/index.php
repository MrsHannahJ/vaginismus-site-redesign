      <section id="pc-muscles">
        <h2>PC kas Grubunun Rolü</h2>
        <h4>Cinsel acıya ya da giriş sorunlarına nasıl katkıda bulunur</h4>
        <div class="wrapper">
          <div class="large-4">
            <p><strong>Vajinismusta etkili olan pelvik taban kaslarına pubococcygeus (PC) kas grubu adı verilir.</strong> PC kas grubunun kadının üreme sisteminde, idrar yolları ve bağırsakları üzerinde çok önemli bir rolü vardır. Bu kaslar kadının idrara çıkmasını, cinsel ilişkide bulunmasını, orgazm yaşamasını, büyük tuvaletini yapmasını ve çocuk doğurmasını sağlar. Bu yüzden bazen onlara pelvik taban kasları, vajina kasları ya da aşk kasları adı da verilir. Vajinismusta zihin ve vücut, girişe karşı bir kas belleği ya da şartlı bir tepki oluşturur. Vücut girişten ötürü acı beklemeyi öğrenmiştir, bu yüzden güçlü PC kası, cinsel birleşme acısı potansiyeline karşı sıkı sıkı kasılır, tıkanır. Bu, bize bir şey atıldığı zaman otomatik olarak gözümüzü kırpmamız ya da çekilmemiz gibidir. Bu, kadının yapmayı düşündüğü bir şey değildir, sadece kendi kendine gerçekleşir.</p>
            <div class="call-out">
              <p>Ne yazık ki, acının önlenmesi yerine, PC kas grubunun kasılması sonuçta ağrıya neden olur: acıya karşı bir korunma mekanizması olarak hareket etse de, tam tersi bir etkiyle sonuçlanır.</p>
            </div>
          </div>
          <div class="large-4">
            <p>Spazmlar girişten ya da hareketten sonra yanma ya da acıya neden olur ve girişi tamamen kapatabilir. PC kas grubu büyük ve çok güçlüdür. İdrar yolu açıklığını, vajinayı ve anüsü sekiz sayısına benzeyen bir şekilde çevreler; kasların bir halkası vajina bölümünün, diğeri ise anüs bölümünün etrafındadır. Bu kaslar, her iki tarafta da, karın ve pelvik organları bir ağ gibi yerinde tutmak için iskelete bağlıdır.</p>
            <figure>
              <img src="../../img/tr/pc-muscle-group-tr.png" alt="PC muscle group">
              <figcaption><strong>PC Kasları:</strong> Kadının pelvik tabanının anatomisi, pubococcygeus ya da PC kas grubu adı verilen iç kasları vurgular. Bu kas grubu, vajinismus yaşandığında istemsiz bir şekilde spazm yapar. Bu güçlü kas grubu hem vajina bölgesinin tamamını, hem de anüsün etrafını çevreler.</figcaption>
            </figure>
          </div>
          <div class="large-4">
            <div class="call-out">
              <p>Etkili program adımları, tüm tetikleyicileri ortadan kaldırmak için hem bedeni hem de zihni kapsamlı şekilde ele alacaktır.</p>
            </div>
            <p>Hiçbir zaman tamamen gevşemeyen, her zaman kısmen kasılı olan PC kasları, ihtiyaç hissettikleri anda harekete geçmeye hazırdır, kadın farkında bile olmadan güçlü bir şekilde sıkışırlar. Örneğin, kadın bunun üzerinde hiç düşünmeden uygun bir zaman gelene dek kadının idrarını ya da büyük tuvaletini tutmasını sağlarlar. Vajinismusta, giriş çabasında bulunulduğunda PC kasları, bilinçli bir niyet (düşünce) olmadan istemsiz olarak sıkışır ve vajinal açıklığı kapatır. Cinsel birleşmeyi rahatsız, acı veren ve erişilmeyen bir şey yapan da bu sıkışmadır. Sık sık neyin neden olduğu bilinmeyen bir acı hissedilir. Bir şeylerin yanlış gittiğini bilen ama sorunun vajinismus olduğunu ve tedavisinin mümkün olduğunu bilmeyen kadın da genelde sıkıntıya düşer.</p>
