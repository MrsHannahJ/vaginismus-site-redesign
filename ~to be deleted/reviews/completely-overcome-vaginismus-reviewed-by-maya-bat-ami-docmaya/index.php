          <h2>Review</h2>
          <h3><em>Completely Overcome Vaginismus &#8211; The Practical Approach to Pain-Free Intercourse</em><br />By Mark and Lisa Carter</h3>
          <p>The intent of the book is to provide a self-help program for women suffering from vaginismus. The book can be read alone but also can be purchased as part of a kit which includes; a companion workbook, a set of vaginal dilators, a video, and a password that allows the reader to participate in an on-line forum with other individuals and couples facing the challenge of vaginismus. The approach of the kit is to provide systematic desensitization exercises for the woman and the couple that include:</p>
          <ul>
            <li>Education about Vaginismus through a video, book and website.</li>
            <li>Communication about Vaginismus through a restricted on-line forum for those who have purchased the kit.</li>
            <li>Normalization of issues that contribute to Vaginismus through the use of statistics and descriptors that let the woman know that she is not alone in having Vaginismus.</li>
            <li>A systematic desensitization format for the woman to use.</li>
            <li>A series of graduated dilators as well as a q-tip and tampon</li>
            <li>A systematic desensitization format for the couple to use.</li>
            <li> Journaling via use of a journal that comes with the kit so that the woman can document her progress.</li>
          </ul>
          <p>This approach is very empowering in that the woman has access to all of the information and can proceed at her own pace through the protocols that are presented. The video and books provide information that can be used by the couple to engender hope and provide steps that allow progress while minimizing anxiety.</p>
          <p>The kit is a wonderful addition to the armamentarium of the Sex Therapist in that it provides specific exercises for the woman and couple to do. The woman can then discuss with the Sex Therapist any issues that arise in the treatment. The couple that has used the kit with this Sex Therapist needed support in addressing the man&#8217;s anxiety about using the kit. The woman was more willing to do the exercises but her husband was having trouble with them. The Sex Therapist was able to work with the couple to help the man apply the techniques of mutual pleasuring and to decrease his avoidance of the exercises suggested in the book.<br /> The multi-dimensional approach of the kit makes available on-going resources that can be used by the woman with vaginismus. One drawback of the book is that while it acknowledges the importance of communication with the partner, the treatment focuses on the woman. Although vaginismus is a diagnosis that applies to the woman, it is a part of the dynamic of the couple&#8217;s sexual interactions. Given the model of women&#8217;s sexuality as embedded in a psycho-relational-cultural context, it would be expected that there would be partner issues that would need to be addressed in the resolution of the vaginismus.</p>
          <p>One way to include the partner in the treatment is to predict that he or she will likely have strong responses to the protocols, thus normalizing any strong reactions that occur. A second way to include the partner is to suggest that he/she keep their own journal about the treatment. The Sex Therapist can then inquire about the partner&#8217;s reactions and journal in such a way that lets the couple know that the relationship will be addressed.</p>
          <p><strong>Reviewed by Maya Bat-Ami, Psy.D &#8211; (website: DocMaya.net)</strong></p>
