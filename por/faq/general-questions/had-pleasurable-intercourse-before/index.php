          <p>O vaginismo pode ocorrer mais tarde na vida, após anos de intercurso sexual prazeroso ou sem dor. Este tipo de vaginismo é chamado de <a href="../primary-and-secondary-vaginismus-differences/index.html">vaginismo secundário</a>. Os <a href="../../../vaginismus-symptoms/index.html">sintomas</a> podem variar bastante desde um simples desconforto, sensações intensas de ardência durante a penetração e até incapacidade de penetração.</p>
          <p>Algumas vezes, a causa não é aparente, outras vezes o vaginismo pode surgir devido a diversas <a href="../../../vaginismus-causes/index.html#non-physical-causes">disfunções médicas/físicas</a>como desconforto por causa de lubrificação inadequada e experiências mais dolorosas como parto, cirurgia, mudanças hormonais ou outras questões de saúde (veja <a href="../../../vaginismus-causes/index.html">Causas</a>). Ele também pode ser desencadeado em resposta à uma situação emocional traumática como, por exemplo, infidelidade do parceiro.</p>
          <h3>Avaliando Quando a Dor Começou</h3>
          <p>Inicialmente, muitas mulheres continuam a ter intercurso sexual mesmo com dor. Mas, eventualmente, devido à dor e ao desconforto, elas podem evitar totalmente o sexo. Algumas vezes, tentar lembrar quando a dor começou inicialmente ajuda a descobrir a causa.</p>
          <p>Se o desencadeamento inicial foi um problema médico ainda não resolvido, a mulher deve procurar tratamento médico. Em geral, recomenda-se tratar as questões médicas básicas antes de centrar-se em resolver o problema do vaginismo.</p>
        
            
