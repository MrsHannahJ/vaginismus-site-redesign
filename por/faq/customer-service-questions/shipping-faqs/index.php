          <h1>Perguntas Frequentes Sobre Envio</h1>
            
          <h4>Quais métodos de envio estão disponíveis?</h4>
          <p>Na hora do pedido, ao escolher o destino do envio online, as opções de envio normalmente disponíveis para o seu país aparecerão. Opções típicas incluem USPS (Correio americano) e DHL Se desejar outras opções além das oferecidas durante este processo, entre em contato conosco diretamente antes de efetuar a transação.</p>

          <h4>De onde os produtos são enviados?</h4>
          <p>Todos os produtos são enviados da Califórnia, EUA.</p>

          <h4>O que aparece na embalagem? O que estará escrito no conhecimento de envio e/ou no formulário de declaração de alfândega?</h4>
          <p>Respeitamos a privacidade com o máximo de sensibilidade e discrição. Tudo no conhecimento de envio é genérico e discreto para proteger a sua privacidade. O seu pedido chegará numa embalagem branca e simples. As palavras “vaginismo”, “vaginismus.com” ou nenhum termo relacionado aparecerão na embalagem ou nos conhecimentos de envio. Um nome de empresa de envio genérico é usado para todos os conhecimentos de envio e em pedidos internacionais com formulários de declaração de alfândega. O nome dos produtos estará como ‘conjunto educacional’ ou outra variação semelhante para proteger a sua privacidade.</p>

          <h4>Os produtos podem ser enviados para o exterior?</h4>
          <p>Sim, enviamos regularmente para o mundo inteiro. A nossa missão é ajudar o máximo de mulheres possível; por isso, estamos dispostos a tentar fazer com que os produtos cheguem em qualquer país, independentemente de quão ‘fechado’ ele for. Para alguns países, pode ser útil ter uma ‘carta de necessidade’ de um médico. Temos enviado com êxito para alguns dos destinos mais remotos do mundo.</p>

          <h4>Haverá alguma taxa alfandegária a ser paga?</h4>
          <p>Em pedidos internacionais (fora dos EUA), cada país tem suas próprias regras individuais para determinar taxas alfandegárias aplicáveis. O cliente é responsável por todas essas taxas e qualquer despesa de processamento de alfândega do correio. No entanto, na maioria dos casos, estas taxas são nominais.</p>

