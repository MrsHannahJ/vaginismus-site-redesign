          <h1>Perguntas Frequentes Sobre Compras e Retornos</h1>
                
          <h3>Comprar PFs</h3>
          <h4>Qual moeda é usada para cobrança quando eu fizer o meu pedido? Como posso calcular a conversão?</h4>
          <p>Todos os preços estão em dólares americanos. Ao fazer o seu pedido, a empresa do seu cartão de crédito realizará a conversão automaticamente. Se você quiser calcular aproximadamente a conversão da moeda antecipadamente, pode usar a <a href="http://www.xe.com/" target="_blank">calculadora gratuita</a> no site.</p>

          <h4>Quais métodos de pagamento são aceitos?</h4>
          <p>Fazer um pedido online por cartão de crédito ou ligar para o número de telefone gratuito são os dois métodos mais rápidos e eficazes de fazer pedidos. Aceitamos pagamento em Visa, Mastercard, American Express, Discover, PayPal, e também podemos processar pedidos manuais via transferência bancária pela Western Union e ordens de pagamento em dólar americano (cheques garantidos ou outros instrumentos monetários). Entre em contato conosco em relação a solicitações de pedidos manuais para obter mais informações.</p>

          <h4>O que é PayPal? Como funciona?</h4>
          <p>Caso não possa ou não deseje pagar diretamente com um cartão de crédito, PayPal é um método alternativo bem popular de processamento de pagamento online seguro (acesse <a href="http://www.paypal.com/" target="_blank">PayPal</a>), o qual permite que os usuários paguem usando diversos métodos diferentes como, por exemplo, contas bancárias, vários cartões de crédito e outras opções. Com uma conta PayPal, você pode encomendar nossos produtos e escolher PayPal como a sua opção de pagamento.</p>

          <h4>Como faço um pedido se não tenho cartão de crédito?</h4>
          <p>Além de cartões de crédito, aceitamos outros métodos de pagamento: PayPal (conta online ou e-cheques), ordem de pagamento em dólar americano (cheques garantidos ou outros instrumentos monetários) ou transferências bancárias através da Western Union. Para solicitar um destes últimos dois serviços, entre em <a href="/por/contact-us/index.php">contato</a> conosco diretamente. Fazemos o máximo possível para garantir uma solução adequada de pagamento.</p>

          <h4>Estou preocupada com a compra pela Internet. O site é seguro?</h4>
          <p>Sim, usamos os melhores métodos de segurança disponíveis para a sua paz de espírito. A Norton Symantec é líder em segurança de compras pela Internet e oferece a criptografia completa e a segurança para a nossa loja online.</p>

          <h3>Retorno PFs</h3>
          <h4>Posso devolver os dilatadores e solicitar um reembolso se eu não usá-los?</h4>
          <p>Não, não podemos aceitar devoluções de dilatadores vaginais. Por motivos de saúde e de segurança, e também para obedecer os regulamentos da FDA, os dilatadores vaginais que forem vendidos a um consumidor não podem ser reabastecidos nem revendidos.</p>

          <h4>Qual é a política de devolução para livros ou vídeos não abertos?</h4>
          <p>Você pode devolver livros ou DVDs não usados dentro de 30 dias a partir da data da compra. Eles devem estar na embalagem original. Você receberá um reembolso completo, menos as despesas de envio e uma taxa de reabastecimento de $10.00. Para solicitar este serviço, entre em contato conosco diretamente.</p>

