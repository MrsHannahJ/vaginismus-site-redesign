            <p><strong>A dor sexual do vaginismo secundário pode afetar as mulheres em todas as fases da vida; mesmo aquelas que, durante muitos anos, tiveram sexo sem dor.</strong></p>
            <p>O vaginismo secundário refere-se à experiência de dor ou dificuldades com a penetração num momento mais tarde na vida, depois de anteriormente ser capaz de ter relação sexual normal e sem dor. Geralmente segue-se a ou é desencadeado por dor pélvica temporária ou outros problemas relacionados. Pode ser desencadeado por doenças, eventos traumáticos, problemas de relacionamento, cirurgia, mudanças de vida (por exemplo, menopausa), ou sem motivo aparente (veja <a href="/por/vaginismus-causes/index.php#non-physical_causes">Causas</a> para mais exemplos). O vaginismo secundário é a causa mais comum de dor sexual frequente ou dificuldade com a penetração, quando não havia nenhum problema anteriormente. Mais comumente, o vaginismo secundário afeta mulheres que têm dor pélvica temporária causada por problemas como infecções urinárias ou fúngicas, dor pelo parto natural, menopausa ou cirurgia.</p>
