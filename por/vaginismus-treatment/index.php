        <p>Colaborando com a transição para o intercurso sexual sem dor, esta etapa explica técnicas de focagem das sensações para casais a fim de reduzir a tensão do assoalho pélvico e aumentar a intimidade. Os casais começam a trabalhar juntos durante esta etapa como um exercício para ensiná-los a praticar bem a focagem das sensações e a se preparar para o intercurso sem dor usando técnicas das etapas anteriores. Os exercícios foram elaborados para criar confiança e compreensão mútua e para apoiar o processo de ajuste até o intercurso sexual sem dor.</p>
