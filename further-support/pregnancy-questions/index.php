          <h1>I can&#8217;t have intercourse and my biological clock is ticking. Any advice?</h1>

          <h3>The pressures of wanting children, but being unable to conceive due to vaginismus, can be overwhelming.</h3>
          <p>One of the more heartbreaking complications of vaginismus, when it exists in forms that <a href="/what-is-vaginismus/vaginismus-symptoms/index.php#unconsummated-marriages">prevent penetration</a> or make sex very difficult, is the inability to conceive a child. The longer a woman lives with vaginismus, the more she may begin to wonder if biological children might ever become a reality in her life. Deep longings to have children may become overwhelming as the years click by.</p>
          <p>Women may feel a sense of desperation, yet the silence surrounding vaginismus often keeps them from explaining to others why they are unable to conceive. Questions from family and friends such as &#8220;Are you planning to have children?&#8221;, &#8220;When are you going to give us a grandchild?&#8221;, &#8220;Isn&#8217;t it about time you started a family?&#8221; and others, can add pressure and further increase a woman&#8217;s feelings of shame, failure, and isolation.</p>

          <h3>Starting Treatment</h3>
          <p>Understanding these pressures, we fully sympathize with those whose clocks are ticking loudly and support them in their efforts to conceive. Where possible, however, we encourage women to continue to work at overcoming vaginismus while they pursue pregnancy options. As vaginismus is usually <a href="../treatment-questions/vaginismus-treatment-success-rates/index.php">treated successfully</a> within a <a href="../treatment-questions/how-long-does-treatment-take/index.php">reasonable time</a>, couples are often able to resolve their vaginismus prior to conception and childbirth.</p>
          <p>Beyond obviously assisting with conception, eliminating the vaginismus problem contributes to a more relaxed pregnancy and a healthier couple relationship in which to parent children. The desire to become pregnant can actually be a major <a href="/further-support/treatment-questions/how-can-i-stay-motivated/index.php">motivator</a> for a woman to complete a vaginismus <a href="/treatment/products/vaginismus-treatment-kit/index.php">program</a> more rapidly.</p>
          <p>Ideally, overcoming vaginismus and experiencing pain-free intercourse in a reasonable time frame will give couples the option for conception by more reliable, natural methods.</p>

          <h3>Alternative Methods for Conception</h3>
          <p>Alternative methods for conception besides penetrative intercourse are possible. Women with vaginismus have become pregnant with either partial intercourse, ejaculation at the vaginal entrance, by using medical/fertility procedures such as intrauterine insemination (IUI), or &#8220;turkey baster&#8221; type methods.</p>
          <p>Compared to normal penetrative intercourse, the chances of getting pregnant by alternative techniques are generally lower. Women should consult with their physicians to discuss these and other potential pregnancy options.</p>

          <h3>Natural Conception</h3>
          <p>For couples who are not faced with extreme time pressures, overcoming vaginismus may be the most practical and viable option and should be considered first before alternative methods of conception.</p>
          <p>Overcoming vaginismus is life-changing and can be a wonderful boost to a couple&#8217;s relationship. <a href="/treatment/index.php">Resolving vaginismus</a> prior to having children allows pregnancy, childbirth, and parenting to be more fully enjoyed without the ongoing stress and pressures of vaginismus in the relationship.</p>

