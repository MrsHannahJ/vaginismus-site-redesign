          <h1>Could my hymen be causing my problem?</h1>
            <h3>Hymen problems rarely cause penetration difficulty.</h3>
            <p>In very rare cases, women with unusually thick hymens may have penetration difficulties because of it. However, in practice, most women who believe their hymen is causing a penetration problem actually have vaginismus. Vaginismus is a much more common cause of penetration difficulties, and the symptoms can be almost identical to that of a hymen problem.</p>
            <p>Women frequently do not realize that their pelvic floor has tightened up the vaginal entrance when they attempt penetration, since with vaginismus this happens involuntarily (the muscles are acting on their own without conscious direction). So they are confused as to why their partner is &#8220;hitting a wall&#8221; with attempts at intercourse, and erroneously conclude that it is a hymen problem.</p>
              
            <h3>Difficulties Getting a Diagnosis</h3>
            <p>Adding further confusion to the issue, many physicians are unfamiliar with both vaginismus and what a normal hymen should look like. If pelvic tightness occurs during a pelvic exam, it may appear to a physician that the <a href="../vagina-too-small/index.html">vagina is abnormally small</a> or that there is a hymen problem requiring surgery. At the same time, if pelvic tightness doesn&#8217;t occur, the vagina may look completely normal and so the physician may be confused as to why there is any penetration problem. The physician may suggest surgery to open up the hymen or vaginal area, falsely believing that this will help with penetration. Note, also, that women in rare circumstances, could have both vaginismus <em>and</em> a hymen problem. This is why a good <a href="../../../vaginismus-diagnosis/index.html">diagnosis</a> from a competent health specialist (who is well-versed on both vaginismus and hymen problems) can be helpful to determine the right course of action.</p>
            <h3>Hymen Abnormalities</h3>
            <p>If there is a physical abnormality with the hymen, it may require a minor surgical procedure (usually called a hymenectomy or hymenotomy) to open or remove the hymen to correct the problem. However, again, this is rarely a necessity.</p>
            <p>We encourage women suspecting hymen problems to complete a gynecological exam with a knowledgeable specialist where issues like this can be discussed, and seek a second opinion when necessary.</p>
