          <h1>Vulvodynia &amp; Vestibulitis</h1>

          <h3>Vulvodynia</h3>
          <p>Vulvodynia is a chronic pain condition characterized by burning, stinging, irritation, and/or rawness in the female genital area, according to <a href="http://www.issvd.org" target="_blank">www.issvd.org</a>. Pain is not always present solely with sexual intercourse or vaginal penetration but typically also occurs during everyday activities (a key way to help differentiate between vaginismus and vulvodynia).</p>

          <h3>Vulvar Vestibulitis Syndrome (VVS)</h3>
          <p>Vulvar vestibulitis is a common form of vulvodynia that has been described as one of the most common causes of genital and sexual pain in women, affecting upwards of 15% [Gardella, 2006]. VVS is specific to pain on touch and/or pressure only in the vestibule. The vulvar vestibule is the area within the inner lips surrounding the vaginal opening. VVS is typically diagnosed using Friedrich&#8217;s Criteria [Friedrich, 1987] which is:</p>
          <ul>
            <li>Severe pain in the vulvar vestibule upon touch or attempted vaginal entry</li>
            <li>Tenderness to pressure localized within the vulvar vestibule</li>
            <li>Vulvar erythema (inflammation) of various degrees</li>
          </ul>
          <p>For diagnosis, a cotton-swab is typically used to place gentle pressure in the vestibule. If VVS is present, the cotton-swab test will often elicit severe pain or discomfort from the woman.</p>
          <p>Like other pain causing conditions, any form of VVS may cause or contribute to problems with vaginismus and/or may coexist with vaginismus on an ongoing basis. As a result, women may need to address both conditions before they are able to fully restore fully pain-free intercourse.</p>

