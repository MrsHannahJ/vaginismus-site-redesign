          <h1>Is there a difference between the treatment for primary and secondary vaginismus?</h1>

          <h3>Vaginismus treatment differs only slightly, depending upon the type and causes.</h3>
          <p>There are only minor differences in the treatment of primary and secondary vaginismus. Women undergoing treatment for secondary vaginismus may find they can bypass certain sections, especially those dealing with <a href="/what-is-vaginismus/vaginismus-causes/index.php#non-physical-causes">emotional triggers</a>, and possibly shorten overall treatment <a href="/further-support/treatment-questions/how-long-does-treatment-take/index.php">duration</a>.</p>
          <p>With secondary vaginismus, the treatment focus is mainly on learning conscious control of the <a href="/what-is-vaginismus/vaginismus-causes/index.php#pc-muscles">PC muscles</a> to eliminate tightness, although other steps are involved. There are usually fewer emotional triggers and so less necessity to work through psychological or emotional issues, unless the circumstances that preceded the vaginismus included experiences of that nature (such as rape or other sexual trauma).</p>

          <h3>Keeping a Positive Outlook</h3>
          <p>As another example, if sex has been painful for a long time or a woman has not had sex for a long time due to the pain, she may have developed some negative feelings towards sex, or possibly her relationship with her partner. In these, or similar cases, it may still be advisable for women with secondary vaginismus to review all the <a href="/treatment/self-help-program/index.php#ten-steps">steps</a> to ensure that all vaginismus triggers are addressed.</p>
          <p>Addressing potential emotional components will ensure that when pain-free intercourse is possible, women are able to fully engage intimately.</p>

          <h3>Treatment for Primary &amp; Secondary Vaginismus</h3>
          <p>Women with complex histories may have difficulty identifying whether they have primary or secondary vaginismus. The overall treatment approach is similar for both, and there is no harm in reviewing every <a href="/treatment/self-help-program/index.php#ten-steps">step</a> in the treatment process. In most cases, women with uncertainty will likely benefit from reviewing all the steps thoroughly and completing a full program.</p>
