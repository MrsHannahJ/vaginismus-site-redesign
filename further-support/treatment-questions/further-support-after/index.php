          <h3>Vaginismus Private Forum</h3>
          <p>Our online private <a href="../../../forum-introduction/index.html">forum</a> is very active (with thousands of posts) and is a safe place for women to receive practical information, solid advice, and encouragement from moderators and other women going through the <a href="../../../products/vaginismus-treatment-kit/index.html">self-help</a> program, or from those who have already completed the program and continue to encourage others.</p>
          <p>Any woman who joins the forum can feel free to post her questions or concerns anonymously by using the screen name of her choice. Some women silently read the posts of others for encouragement and some become active participants in the many encouraging thread discussions.</p>
          <p>The primary goal of the forum is to provide a network of support for women who are actively working to overcome vaginismus. Our forum is available 24 hours a day and women can ask questions about physical or emotional issues as well as gain much needed positive encouragement as they work through the program steps.</p>
          <p>Examples of topics covered in our forum include questions specific to each step of the <a href="../../../products/vaginismus-treatment-kit/index.html">self-help</a> program, relationship issues, emotional issues, pregnancy, religious and social issues surrounding vaginismus, personal stories, gynecological exams, 40+ age related issues, and many other vaginismus-related chat topics. The forum also has an area for spouses. See our <a href="../../../forum-introduction/index.html">Forum</a> page for more information.</p>
          <h3>Private Messaging</h3>
          <p>Private messaging can be used to contact any of the other women in the forum to ask questions or form online friendships or affinity groups.</p>

          <h3>Direct Access to Forum Moderators</h3>
          <p>Our forum moderators are available to answer any questions from any forum members via posts, private messages, or other methods.</p>
