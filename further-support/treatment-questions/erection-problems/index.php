          <h1>My husband is having problems with keeping an erection. Any advice?</h1>

          <h3>Erection difficulties are experienced by a significant number of men.</h3>
          <p>In some cases the problem may result from, or be amplified by, interaction with a spouse who has vaginismus. It is not unusual for some couples who have struggled to overcome vaginismus to also face erectile difficulties (ED).</p>
          <p>Going from an environment of &#8220;no sexâ€”it hurts&#8221; to performance on demand (maintaining an erection and slowly inserting on cue) can be a difficult transition to make. If erectile issues are persistent and do not appear to be resolving on their own, medical attention should be considered.</p>
          <h3>Consulting With a Doctor</h3>
          <p>When erectile difficulties are perceived to be due to the emotional stress of the vaginismus condition, a medical exam is still recommended to potentially eliminate any physical issues that may be contributing.</p>
          <p>If possible, it may be helpful for men to obtain a referral to a urologist specializing in ED. Counseling with a specialist who is familiar with both vaginismus and ED may also be beneficial.</p>
          <h3>Meeting Needs of Both Individuals</h3>
          <p>Careful attention to balancing the needs of each partner will help couples to positively transition through this time. Couples should balance the needs of the male to have a reduction in performance pressure with the needs of the female to practice intercourse to fully overcome vaginismus.</p>
          <p>Some couples find that temporary use of medication to treat ED and allowing a period of time (e.g. two weeks) to engage in intimacy free of pressure to have intercourse is helpful in addressing erectile difficulties. Exercising more and reducing overall stress and anxiety may also be helpful.</p>


