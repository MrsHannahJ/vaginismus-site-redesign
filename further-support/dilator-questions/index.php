          <h3>Dilators Help Control the PC Muscles</h3>
          <p>When used properly, <a href="/treatment/products/vaginal-dilator-set/index.php">vaginal dilators</a> are very helpful with certain parts of vaginismus treatment. Vaginal Dilators can be very effective tools in helping to eliminate the <a href="/what-is-vaginismus/vaginismus-causes/index.php#pc-muscles">PC muscle</a> reflex, the underlying cause of vaginismus tightness, burning, and penetration difficulties. They help enable women to have precise control over the size, speed, and angle of insertions and provide a substitute means to trigger the PC reflex in a sex-like context.</p>
          <p>Together with appropriate exercises, as women consciously and consistently squeeze and relax the PC muscles with dilator insertions, they learn how to override the involuntary muscle contractions that had previously caused tightness or closed the entrance to the vagina to sex. The process helps create new &#8220;muscle memories.&#8221;</p>
          <p>Through the proper use of dilators, women can more easily develop control over involuntary tightness and simultaneously desensitize their vaginal muscles, body and mind to the sensation of having something in their vagina.</p>
          <p>This is all done as transition preparation for inserting the &#8220;real thing&#8221; (i.e. the man&#8217;s penis) without pain or tightness. Together with appropriate exercises, they help women retrain their bodies to respond correctly to penetration and to transition to fully pain-free intercourse.</p>

          <h3>Stretching vs Gaining Control</h3>
          <p>Note that there is more to treating vaginismus then simply inserting dilators. Contrary to popular believe, the focus of dilator use in treating vaginismus is not to simply stretch the vaginal tissues or vaginal opening, but rather to assist women to <em>gain control</em> of their pelvic floor.</p>
          <p>Women with <a href="/what-is-vaginismus/vaginismus-symptoms/index.php#unconsummated-marriages">penetration difficulties</a> related to their vaginismus often mistakenly assume that dilators are used to stretch the vaginal opening so that it will be larger, when in fact, their penetration problems are not related to the size of the vagina but rather to problems with involuntary tightness of the <a href="/what-is-vaginismus/vaginismus-causes/index.php#pc-muscles">pelvic floor</a>. Dilators together with pelvic floor exercises are used to gain control over the involuntary tightness.</p>
          <p>The name vaginal dilator is actually a bit of a misnomer, since dilator use is not normally focused on dilation (which means to make wider or larger). For this reason, some specialists refer to them as vaginal trainers or spacers. There are also alternatives to dilator use, but in practice women usually find dilators to be the easiest aid for completing the necessary pelvic floor exercises.</p>

          <h3>Dual Purpose Situations</h3>
          <p>A portion of women, especially those who are recovering from <a href="/further-support/related-conditions/vaginal-atrophy-resources/index.php">pelvic surgery or suffering from age related hormone changes like menopause</a>, have a dual purpose for the dilators, both in the rehabilitative stretching of vaginal tissues and the rehabilitation of the pelvic floor musculature responses (tightness from vaginismus). <a href="/treatment/products/vaginal-dilator-set/index.php">Dilators</a> are helpful aids that are most effective when part of a complete <a href="/treatment/self-help-program/index.php#ten-steps">process</a> with proper physical instruction and transition steps.</p>

