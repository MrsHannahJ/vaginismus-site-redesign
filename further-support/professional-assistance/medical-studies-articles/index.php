            <li>Lankveld, J., Everaerd, W., &amp; Grotjohann, Y. (2001). Cognitive-behavioral bibliotherapy for sexual dysfunctions in heterosexual couples: A randomized waiting-list controlled clinical trial in the Netherlands. <em>J Sex Res, 38</em>(1), 51-67. Study indicated that the &#8216;minimal therapist support&#8217; approach for the vaginismus condition resulted in lower complaints of vaginismus posttreatment.</li>
            <li>Lewis, R., Fugl-Meyer, K., Bosch, R., Fugl-Meyer, A., Laumann, E., Lizza, E., &amp; Marin-Morales, A. (2004). Epidemiology/risk factors of sexual dysfunction. <em>J Sex Med, 1</em>(1), 35-39.</li>
