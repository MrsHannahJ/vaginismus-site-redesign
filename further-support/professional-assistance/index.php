            <h2>Medical Studies &#038; Articles</h2>
            <p>The following list includes a variety of medical studies and articles of special interest to those researching or studying about vaginismus.</p>
            <ol>
              <li>Biswas, A., &amp; Ratnam, S. (1995). Vaginismus and outcome of treatment. <em>Ann Acad Med Singapore, 24</em>(5), 755-758. This article discusses the rapid desensitization method of vaginismus treatment.</li>
              <li>Butcher, J. (1999). ABC of sexual health: Female sexual problems II: Sexual pain and sexual fears. <em>BMJ</em>, 318, 110-112. Outlines cognitive and behavioral treatment for vaginismus.</li>
              <li>de Marquiegui, A., &amp; Huish, M. (1999). ABC of sexual health: A woman&#8217;s sexual life after an operation. <em>BMJ</em>, 318, 178-181.</li>
              <li>Heiman, J. (2002). Sexual dysfunction: Overview of prevalence, etiological factors, and treatments. <em>J Sex Res, 39</em>(1), 73-78.</li>
              <li>K.K. (2001, August). I couldn&#8217;t have sex—at all. <em>Glamour</em>, 100.</li>
              <li>Lankveld, J., Everaerd, W., &amp; Grotjohann, Y. (2001). Cognitive-behavioral bibliotherapy for sexual dysfunctions in heterosexual couples: A randomized waiting-list controlled clinical trial in the Netherlands. <em>J Sex Res, 38</em>(1), 51-67. Study indicated that the &#8216;minimal therapist support&#8217; approach for the vaginismus condition resulted in lower complaints of vaginismus posttreatment.</li>
              <li>Lewis, R., Fugl-Meyer, K., Bosch, R., Fugl-Meyer, A., Laumann, E., Lizza, E., &amp; Marin-Morales, A. (2004). Epidemiology/risk factors of sexual dysfunction. <em>J Sex Med, 1</em>(1), 35-39.</li>
              <li>Nasab, M., &amp; Farnoosh, Z. (2003). Management of vaginismus with cognitive-behavioral therapy, self-finger approach: A study of 70 cases. <em>IJMS, 28</em>(2), 69-71.</li>
              <li>Phillips, N. (2000). Female sexual dysfunction: Evaluation and treatment. <em>Am Fam Physician</em>, 62, 127-36, 141-42.<br /> Well-written article outlining the differential diagnosis of vaginismus and other sexual dysfunctions.</li>
              <li>Reissing, E., Binik, Y., &amp; Khalife, S. (1999). Does vaginismus exist? A critical review of the literature. <em>J Nerv Ment Dis, 187</em>(5), 261-74. Authors conclude after an examination of the literature that contrary to popular opinion, vaginismus is not easily diagnosed.</li>
              <li>Reissing, E., Binik, Y., Khalife, S., Cohen, D., &amp; Amsel, R. (2004). Vaginal spasm, pain and behavior: An empirical investigation of the diagnosis of vaginismus. <em>Arch Sex Behav, 33</em>(1), 5-17.</li>
              <li>Reissing, E., Binik, Y., Khalife, S., Cohen, D., &amp; Amsel, R. (2003). Etiological correlates of vaginismus: Sexual and physical abuse, sexual knowledge, sexual self-schema, and relationship adjustment. <em>J Sex Marital Ther, 29</em>(1), 47-59. Discusses the importance of how the vaginismus diagnosis needs to include pain and avoidance of pain as part of the patient history.</li>
              <li>Schnyder, U., Schnyder-Luthi, C., Ballinari, P., &amp; Blaser, A. (1998). Therapy for vaginismus: In vivo versus in vitro desensitization. <em>Can J Psychiatry, 43</em>(9), 941-44. Authors found that desensitization exercises with dilators was an effective method for treating vaginismus and that self-insertion versus therapist-insertion of dilators was equally successful.</li>
              <li>Scholl, G. (1988). Prognostic variables in treating vaginismus. <em>Obstet Gynecol</em>, 72, 231-35.</li>
              <li>Walton, B., &amp; Thorton, T. (2003). Female sexual dysfunction. <em>Current Women&#8217;s Health Reports</em>, 3, 319-326.</li>
              <li>Watson, J., &amp; Davies, T. (1997). ABC of mental health: Psychosexual problems. <em>BMJ, 315</em>, 239-42. Information includes emotional components of vaginismus.</li>
