            <h2>Vaginismo Primario</h2>
            <p><strong>Cuando una mujer nunca ha podido tener actividad sexual sin dolor debido a la constricción causada por el vaginismo, su padecimiento se conoce como vaginismo primario.</strong></p>
            <p>El vaginismo primario es la experiencia de vaginismo durante los ‘primeros’ intentos de relación sexual. Típicamente, el vaginismo primario se descubre cuando una mujer intenta tener sexo por primera vez. El esposo / la pareja no es capaz de lograr la penetración y es como si se topara con una ‘pared’ en donde debería estar la abertura a la vagina. La penetración no es posible o es extremadamente difícil. Algunas mujeres con vaginismo primario también experimentan problemas con la inserción de un tampón o con los exámenes ginecológicos. Los músculos PC constriñen y contraen la abertura vaginal, haciendo la penetración incómoda o, en muchos casos, prácticamente imposible.</p>
