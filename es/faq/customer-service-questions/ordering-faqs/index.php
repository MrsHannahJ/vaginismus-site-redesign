          <h3>Pedidos PMFs</h3>
          <h4>¿Qué moneda se utiliza para facturar mi pedido? ¿Cómo puedo calcular la conversión?</h4>
          <p>Todos los precios son cotizados en dólares de los Estados Unidos de América. En el momento de hacer su pedido, la compañía de su tarjeta de crédito realizará automáticamente la conversión para usted. Si desea hacer un cálculo aproximado de la conversión, puede utilizar el <a href="http://www.xe.com/" target="_blank">calculador gratuito en </a>.</p>

          <h4>¿Que métodos de pago aceptan?</h4>
          <p>Los métodos más rápidos y eficientes para hacer su pedido es por internet utilizando su tarjeta de crédito o llamando al número gratuito. Aceptamos pagos hechos con Visa, MasterCard, American Express, Discover, PayPal y también podemos procesar pedidos manualmente por medio de transferencias con Western Union y giros en dólares estadounidenses enviados por correo postal (cheques garantizados u otros instrumentos monetarios). Contáctenos si desea información relacionada a pedidos realizados manualmente.</p>

          <h4>¿Qué es PayPal? ¿Cómo trabaja?</h4>
          <p>Para aquellas personas que no pueden o no desean pagar directamente con una tarjeta de crédito, paypal es una alternativa popular de procesamiento seguro de pagos en línea (vea <a href="http://paypal.com/" target="_blank">PayPal</a>).PayPal permite a los usuarios enviar pagos utilizando diversos métodos, incluyendo cuentas bancarias, varias tarjetas de crédito y otras opciones. Cuando tiene una cuenta de paypal, usted puede ordenar nuestros productos en línea simplemente eligiendo esta opción de pago.</p>

          <h4>¿Cómo realizo un pedido si no tengo una tarjeta de crédito?</h4>
          <p>Además de tarjetas de crédito, aceptamos otros métodos de pago: PayPal (cuenta en línea o cheques electrónicos), giros en dólares estadounidenses (cheques garantizados u otros instrumentos monetarios), o transferencias por medio de Western Union. Para solicitar alguno de los últimos dos servicios, <a href="/es/faq/customer-service-questions/../../contact-us/index.php">contáctenos</a>. Haremos todo lo posible para asegurar lograr una solución factible para todas las mujeres que tienen necesidad.</p>

          <h4>Me inquieta poner mi pedido por la Internet. ¿Es su sitio Web seguro?</h4>
          <p>Sí, para su tranquilidad utilizamos los mejores métodos de seguridad disponibles. Norton Symantec es un líder reconocido en el campo de la seguridad del comercio por internet y brinda la encriptación y seguridad de nuestra entera tienda en línea.</p>

          <h4>¿Ustedes garantizan los precios más bajos?</h4>
          <p>Sí, garantizamos que nuestros precios para nuestros productos son los más bajos que están disponibles a nivel global. Ya que nuestro objetivo es ayudar a mujeres, ofrecemos sets de dilatadores vaginales de calidad y grado médico al mejor precio del mundo, y también ofrecemos el costo más bajo y los programas para el vaginismo más completos disponibles.</p>
          
          <h3>Devolución PMFs</h3>
          <h4>¿Puedo regresar los dilatadores y recibir un reembolso si no los utilicé?</h4>
          <p>No, no podemos aceptar devoluciones de los dilatadores vaginales. Por razones de salud y seguridad, y con el fin de cumplir con las normas del FDA (Administración de Drogas y Alimentos de los Estados Unidos) para los insertos vaginales, los dilatadores vaginales que se hayan vendido a un consumidor no pueden regresar a inventario y se prohíbe su venta.</p>

          <h4>¿Cuál es la política de devolución para libros o videos que no han sido abiertos?</h4>
          <p>Usted puede devolver cualquier libro o video no utilizado en su empaque original en un periodo de 30 días para recibir un crédito por la cantidad completa, menos los costos de envío y manejo y una tarifa de reposición de existencias de $10.00. Para solicitar este servicio contáctenos directamente.</p>

