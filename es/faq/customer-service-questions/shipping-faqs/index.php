          <h1>Preguntas Más Frecuentes Sobre El Envío</h1>
            
          <h4>¿Qué métodos de envío están disponibles?</h4>
          <p>En el momento de poner su pedido, cuando elija la destinación del envío en línea, las opciones de envío normalmente disponibles para esos países aparecerán. Opciones frecuentes incluyen el servicio de envío de correo postal de los Estados Unidos (USPS) y DHL. Si usted desea otras opciones distintas a las que ofrecemos durante este proceso, favor de contactarnos directamente antes de finalizar la transacción.</p>

          <h4>¿De dónde envían sus productos?</h4>
          <p>Todos los productos son enviados desde California, Estados Unidos.</p>

          <h4>¿Qué aparece en el empaque? ¿Qué dirá en el documento de embarque o en el formulario de declaración de aduanas?</h4>
          <p>Nuestra misión es ayudar a parejas con problemas de dolor sexual. Por esta razón, hacemos grandes esfuerzos para asegurar la privacidad y confidencialidad de nuestros clientes. Su pedido llegará en un paquete no impreso. Ni las palabras vaginismus, vaginismus.com ni ningún otro término relacionado aparecerá en la caja o en el documento de embarque. Se usará el nombre de una compañía genérica de envíos. En los formularios de declaración de aduanas, el nombre de los productos aparecerá como kit educacional, piezas plásticas u otra variante similar a fin de proteger la privacidad.</p>

          <h4>¿Hacen envíos internacionales?</h4>
          <p>Sí, hacemos envíos a todo el mundo. Como nuestro objetivo es ayudar al mayor número posible de mujeres con vaginismo, estamos dispuestos a intentar enviar los productos a cualquier país, sin importar qué tan “cerrados” se encuentren. Para algunos países, una “carta de necesidad” de un médico puede ser de utilidad. Hemos realizado envíos exitosamente a algunos de los destinos más remotos del mundo.</p>

          <h4>¿Habrán cargos de aduanas o impuestos?</h4>
          <p>Para pedidos internacionales (fuera de los Estados Unidos), cada país tiene sus propias normas para determinar los cargos de aduanas, impuestos o pagos que apliquen. El cliente es responsable de dichos cargos y de cualquier cargo por envío de aduanas que aplique. Cada país cobra sus propias tarifas de aduanas.</p>
