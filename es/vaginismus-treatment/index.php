        <p>El dolor sexual femenino y las dificultades de penetración típicamente incluyen cierto grado de constricción involuntaria del piso pélvico. Este paso se enfoca en el papel de los músculos del piso pélvico, especialmente el grupo muscular pubococcígeos (PC) explicando en gran detalle cómo es que una vez desencadenados pasan a causar constricción involuntaria al intentar el coito. El efectivo tratamiento del vaginismo se enfoca en reentrenar el piso pélvico para eliminar las reacciones involuntarias del músculo que produce la constricción o dolor. Aprender a identificar, controlar selectivamente, ejercitar y reentrenar los músculos pélvicos para reducir el dolor y aliviar la constricción y dificultades de penetración es un paso importante en el tratamiento del vaginismo.</p>
